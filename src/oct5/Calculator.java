package oct5;

public class Calculator {

    public int sum(int a, int b){
        int result = a + b;
        return result;
    }

    public int subtraction(int a, int b){
        int result = a - b;
        return result;
    }

    public int division(int a, int b){
        int result = a / b;
        return result;
    }

    public int multiply(int a, int b){
        int result = a * b;
        return result;
    }
}
